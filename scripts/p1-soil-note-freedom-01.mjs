import { readFile, writeFile } from 'node:fs/promises';

const root = new URL('../', import.meta.url);

async function read(path) {
  return readFile(new URL(path, root), 'utf8');
}

async function write(path, content) {
  await writeFile(new URL(path, root), content);
}

function replaceOnce(source, pattern, replacement, label) {
  const matches = source.match(pattern);
  if (!matches) throw new Error(`missing patch target: ${label}`);
  const after = source.replace(pattern, replacement);
  if (after === source) throw new Error(`unchanged patch target: ${label}`);
  return after;
}

